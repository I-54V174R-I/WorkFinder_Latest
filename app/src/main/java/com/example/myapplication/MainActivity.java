public class MainActivity extends AppCompatActivity{

    public static final String usuarioEmail="email";

    //se declaran las variables para los textview
    private TextView email, name;

    private AppBarConfiguration mAppBarConfiguration;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        Toolbar toolbar = findViewById(R.id.toolbar);

        setSupportActionBar(toolbar);

        DrawerLayout drawer = findViewById(R.id.drawer_layout);

        NavigationView navigationView = findViewById(R.id.nav_view);
        // Passing each menu ID as a set of Ids because each
        // menu should be considered as top level destinations.
        mAppBarConfiguration = new AppBarConfiguration.Builder(
                R.id.nav_home, R.id.nav_gallery, R.id.nav_slideshow, R.id.nav_servicio, R.id.nav_capital, R.id.nav_profile, R.id.nav_config)
                .setDrawerLayout(drawer)
                .build();
        NavController navController = Navigation.findNavController(this, R.id.nav_host_fragment);
        NavigationUI.setupActionBarWithNavController(this, navController, mAppBarConfiguration);
        NavigationUI.setupWithNavController(navigationView, navController);

        LayoutInflater inflater = (LayoutInflater)this.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
        View view = inflater.inflate(R.layout.nav_header_main, navigationView);

        name = (TextView)view.findViewById(R.id.nombremenu);
        email =(TextView)view.findViewById(R.id.emailmenu);

        String user = getIntent().getStringExtra("email");



        email.setText(user);

        if (user.contains("abigail")){
            name.setText("Abigail Lopez");
        }
        else {
            if (user.contains("mario")){
                name.setText("Mario Villagomez");
            }
            else {
                if (user.contains("sergio")){
                    name.setText("Sergio Rodriguez");
                }
                else{
                    if (user.contains("luis")){
                        name.setText("Luis Fernandez");
                    }
                }

            }
        }
    }
    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onSupportNavigateUp() {
        NavController navController = Navigation.findNavController(this, R.id.nav_host_fragment);
        return NavigationUI.navigateUp(navController, mAppBarConfiguration)
                || super.onSupportNavigateUp();
    }


}